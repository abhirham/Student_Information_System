    public String getdate(Date a){
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String s = df.format(a);
        return s;
    }
    
    public boolean checksid(){
        String sql = "select max(STU_ID) as 'sid' from STUDENT_TORONTO";
        int id=20;
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                id = Integer.parseInt(rs.getString("sid"));
             }
       } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
             if(id<Integer.parseInt(sid.getText()) || Integer.parseInt(sid.getText())<100000)
                return false;
            else
                return true;
        
    }

